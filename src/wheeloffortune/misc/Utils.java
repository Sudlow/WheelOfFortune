package wheeloffortune.misc;

public class Utils {

	// Make this private so you can't say 'new Utils()'
	private Utils() {
	}
	
	public static int square(int n) {
		return n * n;
	}
	
	public static float square(float n) {
		return n * n;
	}
	
	public static double square(double n) {
		return n * n;
	}
	
}
